import fs from "node:fs";
import path from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const countriesLib = require("i18n-iso-countries");
countriesLib.registerLocale(require("i18n-iso-countries/langs/en.json"));

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

const COORD_OVERRIDES = {
  "GR:Athens": [37.9838, 23.7275],
  "CZ:Prague": [50.0911, 14.4016],
  "GB:Glasgow City": [55.8627, -4.2311],
  "FR:France": [46.2276, 2.2137],
  "IT:Italy": [41.8719, 12.5674],
  "ES:Spain": [40.4637, -3.7492],
  "US:United States": [39.8283, -98.5795],
  "DE:Germany": [51.1657, 10.4515],
};

function norm(value) {
  return value.toLowerCase().replace(/[^a-z0-9]/g, "");
}

function countryName(code) {
  const official = countriesLib.getName(code, "en", { select: "official" }) ?? code;
  const alias = countriesLib.getName(code, "en", { select: "alias" }) ?? official;
  if (alias.toLowerCase() === official.toLowerCase()) return alias;
  return `${alias} (${official})`;
}

function loadTouristCities() {
  const csv = fs.readFileSync(path.join(root, "lib/data/tourist-cities.csv"), "utf8").trim();
  return csv
    .split("\n")
    .slice(1)
    .map((line) => {
      const [countryCode, name, latitude, longitude] = line.split(",");
      return {
        countryCode,
        name,
        latitude: Number.parseFloat(latitude),
        longitude: Number.parseFloat(longitude),
      };
    });
}

function findCoords(code, lookupName, touristCities) {
  const key = `${code}:${lookupName}`;
  const override = COORD_OVERRIDES[key];
  if (override) {
    return { cityName: lookupName, latitude: override[0], longitude: override[1] };
  }

  const target = norm(lookupName);
  const matches = touristCities.filter((city) => city.countryCode === code);
  const exact = matches.find((city) => norm(city.name) === target);
  if (exact) {
    return { cityName: exact.name, latitude: exact.latitude, longitude: exact.longitude };
  }

  const fuzzy = matches.find((city) => {
    const name = norm(city.name);
    return name.includes(target) || target.includes(name);
  });
  if (fuzzy) {
    return { cityName: fuzzy.name, latitude: fuzzy.latitude, longitude: fuzzy.longitude };
  }

  return null;
}

const touristCities = loadTouristCities();
const source = JSON.parse(
  fs.readFileSync(path.join(__dirname, "popular-destinations-source.json"), "utf8")
);

const destinations = [];
const missing = [];

for (const entry of source) {
  const kind = entry.kind === "country" ? "country" : "city";
  const code = entry.countryCode.toUpperCase();
  const label = entry.label;

  if (kind === "country") {
    const override = COORD_OVERRIDES[`${code}:${label}`];
    if (!override) {
      missing.push(`${code} country ${label}`);
      continue;
    }
    destinations.push({
      kind: "country",
      countryCode: code,
      cityName: label,
      label,
      countryName: countryName(code),
      latitude: override[0],
      longitude: override[1],
    });
    continue;
  }

  const lookup = entry.lookup ?? label;
  const coords = findCoords(code, lookup, touristCities);
  if (!coords) {
    missing.push(`${code} ${lookup}`);
    continue;
  }

  destinations.push({
    kind: "city",
    countryCode: code,
    cityName: coords.cityName,
    label,
    countryName: countryName(code),
    latitude: coords.latitude,
    longitude: coords.longitude,
  });
}

if (missing.length > 0) {
  console.warn("Missing coordinates for:", missing.join(", "));
  process.exitCode = 1;
}

const output = `// Auto-generated by scripts/build-popular-destinations.mjs — do not edit by hand.

export type PopularDestination = {
  kind: "city" | "country";
  countryCode: string;
  cityName: string;
  label: string;
  countryName: string;
  latitude: number;
  longitude: number;
};

export const POPULAR_DESTINATIONS: PopularDestination[] = ${JSON.stringify(destinations, null, 2)};

/** @deprecated Use POPULAR_DESTINATIONS — kept for import compatibility */
export const FEATURED_DESTINATIONS: PopularDestination[] = POPULAR_DESTINATIONS;

/** @deprecated Capitals list removed — popular list is a single curated ranking */
export const CAPITAL_DESTINATIONS: PopularDestination[] = [];
`;

const outPath = path.join(root, "lib/data/popular-destinations.ts");
fs.writeFileSync(outPath, output, "utf8");
console.log(`Wrote ${destinations.length} destinations to ${outPath}`);
