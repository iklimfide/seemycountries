import fs from "node:fs";
import path from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const countriesLib = require("i18n-iso-countries");
countriesLib.registerLocale(require("i18n-iso-countries/langs/en.json"));

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function norm(value) {
  return value.toLowerCase().replace(/[^a-z0-9]/g, "");
}

function countryName(code) {
  const official = countriesLib.getName(code, "en", { select: "official" }) ?? code;
  const alias = countriesLib.getName(code, "en", { select: "alias" }) ?? official;
  if (alias.toLowerCase() === official.toLowerCase()) return alias;
  return `${alias} (${official})`;
}

function loadTouristParks() {
  const csv = fs.readFileSync(path.join(root, "lib/data/tourist-parks.csv"), "utf8").trim();
  return csv
    .split("\n")
    .slice(1)
    .map((line) => {
      const match = line.match(
        /^([^,]+),([^,]+),"((?:[^"]|"")*)","((?:[^"]|"")*)",([^,]+),([^,]+)$/
      );
      if (!match) return null;
      const [, parkType, countryCode, countryNameRaw, nameRaw, latitude, longitude] = match;
      return {
        parkType,
        countryCode,
        countryName: countryNameRaw.replace(/""/g, '"'),
        name: nameRaw.replace(/""/g, '"'),
        latitude: Number.parseFloat(latitude),
        longitude: Number.parseFloat(longitude),
      };
    })
    .filter(Boolean);
}

function findPark(code, parkType, lookupName, parks) {
  const target = norm(lookupName);
  const matches = parks.filter(
    (park) => park.countryCode === code && park.parkType === parkType
  );
  const exact = matches.find((park) => norm(park.name) === target);
  if (exact) return exact;
  return (
    matches.find((park) => {
      const name = norm(park.name);
      return name.includes(target) || target.includes(name);
    }) ?? null
  );
}

const touristParks = loadTouristParks();
const source = JSON.parse(
  fs.readFileSync(path.join(__dirname, "popular-parks-source.json"), "utf8")
);

const destinations = [];
const missing = [];

for (const entry of source) {
  const parkType = entry.parkType;
  const code = entry.countryCode.toUpperCase();
  const label = entry.label;
  const lookup = entry.lookup ?? label;

  if (
    entry.latitude != null &&
    entry.longitude != null &&
    Number.isFinite(entry.latitude) &&
    Number.isFinite(entry.longitude)
  ) {
    destinations.push({
      parkType,
      countryCode: code,
      parkName: entry.parkName ?? label,
      label,
      countryName: countryName(code),
      latitude: entry.latitude,
      longitude: entry.longitude,
    });
    continue;
  }

  const park = findPark(code, parkType, lookup, touristParks);

  if (!park) {
    missing.push(`${parkType} ${code} ${lookup}`);
    continue;
  }

  destinations.push({
    parkType,
    countryCode: code,
    parkName: park.name,
    label,
    countryName: countryName(code),
    latitude: park.latitude,
    longitude: park.longitude,
  });
}

if (missing.length > 0) {
  console.warn("Missing popular parks:", missing.join("; "));
}

const tsPath = path.join(root, "lib/data/popular-parks.ts");
const tsContent = `// Auto-generated by scripts/build-popular-parks.mjs — do not edit by hand.

import type { ParkType } from "@/lib/data/tourist-park-search";

export type PopularPark = {
  parkType: ParkType;
  countryCode: string;
  parkName: string;
  label: string;
  countryName: string;
  latitude: number;
  longitude: number;
};

export const POPULAR_PARKS: PopularPark[] = ${JSON.stringify(destinations, null, 2)};
`;
fs.writeFileSync(tsPath, tsContent, "utf8");
console.log(`Wrote ${destinations.length} popular parks to ${tsPath}`);
